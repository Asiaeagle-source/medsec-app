select h.id, p.id, src.role::public.hospital_assignment_role, src.is_primary, src.source
from src
join public.hospitals h on h.copi01_code = src.copi01_code
join public.profiles  p on p.employee_id = src.employee_id
on conflict (hospital_id, staff_id, role) do nothing;

